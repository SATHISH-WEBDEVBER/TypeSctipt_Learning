// Interface Example - JavaScript

// JavaScript does not support interfaces.
// JavaScript-ல் interface என்ற feature இல்லை.

// Creating a user object.
// ஒரு user object உருவாக்கப்படுகிறது.
var user = {
  id: 1,
  name: "Sathish",
  isActive: true
};

// Display the object.
// object-ஐ காட்டுகிறது.
console.log(user);

// JavaScript allows adding new properties.
// JavaScript-ல் புதிய properties சேர்க்கலாம்.
user.email = "sathish@gmail.com";

console.log(user);