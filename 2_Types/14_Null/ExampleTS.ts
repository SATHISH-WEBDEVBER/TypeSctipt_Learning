// Null Example - TypeScript

// Creating a variable that can store a string or null.
// string அல்லது null value-ஐ சேமிக்கும் variable உருவாக்கப்படுகிறது.
let selectedUser1: string | null = null;

// Display the value.
// value-ஐ காட்டுகிறது.
console.log(selectedUser1);

// Assigning a string value.
// string value assign செய்யப்படுகிறது.
selectedUser1 = "Sathish";

console.log(selectedUser1);