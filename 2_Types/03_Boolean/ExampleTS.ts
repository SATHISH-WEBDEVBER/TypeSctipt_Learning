// Boolean Type Example - TypeScript

// Creating a variable with boolean datatype.
// boolean datatype-உடன் variable உருவாக்கப்படுகிறது.
var isLoggedIn: boolean = true;

// Display the current value.
// தற்போதைய மதிப்பை காட்டுகிறது.
console.log(isLoggedIn);

