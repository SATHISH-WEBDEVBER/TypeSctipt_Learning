// Undefined Example - TypeScript

// Creating a variable that can store a string or undefined.
// string அல்லது undefined value-ஐ சேமிக்கும் variable உருவாக்கப்படுகிறது.
let userName1: string | undefined;

// Display the value.
// value-ஐ காட்டுகிறது.
console.log(userName1);

// Assigning a string value.
// string value assign செய்யப்படுகிறது.
userName1 = "Sathish";

console.log(userName1);