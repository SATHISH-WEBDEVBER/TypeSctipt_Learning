// Object Type Example - JavaScript

// Creating an object to store related information.
// தொடர்புடைய தகவல்களை சேமிக்க object உருவாக்கப்படுகிறது.
var student = {
name: "Sathish",
age: 21
};

// Display the object.
// object-ஐ காட்டுகிறது.
console.log(student);

// JavaScript allows changing the datatype of a property.
// JavaScript-ல் property datatype-ஐ மாற்ற அனுமதி உள்ளது.
student.age = "Twenty One";

// Display the updated object.
// மாற்றப்பட்ட object-ஐ காட்டுகிறது.
console.log(student);
