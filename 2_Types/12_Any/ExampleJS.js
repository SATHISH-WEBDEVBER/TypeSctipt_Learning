// Any Example - JavaScript

// JavaScript variables can hold any datatype.
// JavaScript variable எந்த datatype-யையும் வைத்திருக்கலாம்.

var value = "Sathish";

console.log(value);

// Changing the datatype.
// datatype மாற்றப்படுகிறது.
value = 100;

console.log(value);

// Changing the datatype again.
// datatype மீண்டும் மாற்றப்படுகிறது.
value = true;

console.log(value);