// Any Example - TypeScript

// Creating a variable with any datatype.
// any datatype-உடன் variable உருவாக்கப்படுகிறது.
let value1: any = "Sathish";

// Display the value.
// value-ஐ காட்டுகிறது.
console.log(value1);

// Assigning a number.
// number value assign செய்யப்படுகிறது.
value1 = 100;

console.log(value1);

