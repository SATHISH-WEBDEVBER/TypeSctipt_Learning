// Array Type Example - JavaScript

// Creating an array containing multiple values.
// பல values-ஐ கொண்ட array உருவாக்கப்படுகிறது.
var fruits = ["Apple", "Orange", "Mango"];

// Display the current array.
// தற்போதைய array-ஐ காட்டுகிறது.
console.log(fruits);

// JavaScript allows adding values of different datatypes.
// JavaScript-ல் வேறு datatype values சேர்க்க அனுமதி உள்ளது.
fruits.push(100);

// Display the updated array.
// மாற்றப்பட்ட array-ஐ காட்டுகிறது.
console.log(fruits);
