// Array Type Example - TypeScript

// Creating an array that contains only strings.
// string values மட்டும் கொண்ட array உருவாக்கப்படுகிறது.
var fruits: string[] = ["Apple", "Orange", "Mango"];

// Display the current array.
// தற்போதைய array-ஐ காட்டுகிறது.
console.log(fruits);

// TypeScript prevents adding a number to a string array.
// string array-க்கு number value சேர்க்க TypeScript அனுமதிக்காது.

