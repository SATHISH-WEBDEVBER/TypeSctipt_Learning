// Strong Typing Example - JavaScript

// Creating a variable and storing a number value.
// ஒரு variable-ஐ உருவாக்கி அதில் number value சேமிக்கப்படுகிறது.
var age = 25;

// Display the current value of age.
// age variable-ன் தற்போதைய மதிப்பை காட்டுகிறது.
console.log(age);

// JavaScript allows changing the datatype.
// JavaScript-ல் datatype-ஐ மாற்ற அனுமதி உள்ளது.
age = "Twenty Five";

// Displays the updated value.
// மாற்றப்பட்ட மதிப்பை காட்டுகிறது.
console.log(age);